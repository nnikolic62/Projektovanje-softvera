    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            RB = 0;
            StavkaTableModel model = (StavkaTableModel) tblStavke.getModel();
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            String dan = txtDan.getText();
            String izvor = txtIzvor.getText();
            if (dan.isEmpty() || izvor.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja popuni");
                return;
            }

            Date datum = sdf.parse(dan);
            List<StavkaKursneListe> stavke = model.vratiListu();
            if(stavke.size() != 4){
                JOptionPane.showMessageDialog(this, "Ima vise od 4");
                return;
            }
            int brojac = 0;
            for (StavkaKursneListe s : stavke) {
                if(s.getValuta().equals("EUR"));
                brojac++;
            }
            for (StavkaKursneListe s : stavke) {
                if(s.getValuta().equals("USD"));
                brojac++;
            }
            for (StavkaKursneListe s : stavke) {
                if(s.getValuta().equals("CHF"));
                brojac++;
            }
            for (StavkaKursneListe s : stavke) {
                if(s.getValuta().equals("GBP"));
                brojac++;
            }
            System.out.println(brojac);
            if(brojac / 4 != 4){
                JOptionPane.showMessageDialog(this, "Ponavlja se");
                return;
            }

            for (StavkaKursneListe s : stavke) {
                System.out.println(s.getRB() + " ");
                if(s.getProdajni() < s.getKupovni()){
                    JOptionPane.showMessageDialog(this, "Ne moze kupovni veci od prodajnog");
                    return;
                }
            }
            
            KursnaLista kl = new KursnaLista();
            kl.setDana(datum);
            kl.setIzvor(izvor);
            kl.setStavke(stavke);
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setArgument(kl);
            kz.setOperacija(Operacija.SACUVAJ);
            Komunikacija.getInstance().posaljiZahtev(kz);
            
            ServerskiOdgovor so = Komunikacija.getInstance().prihvatiOdgovor();
            String poruka = so.getPoruka();
            
            JOptionPane.showMessageDialog(this, poruka);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

