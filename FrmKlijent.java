    private void srediTabelu() {
        StavkaTableModel model = new StavkaTableModel();
        tblStavke.setModel(model);

        KlijentskiZahtev kz = new KlijentskiZahtev();
        kz.setOperacija(Operacija.VRATI_ZEMLJE);
        Komunikacija.getInstance().posaljiZahtev(kz);

        ServerskiOdgovor so = Komunikacija.getInstance().prihvatiOdgovor();
        List<Zemlja> list = (List<Zemlja>) so.getRezultat();

        JComboBox cb = new JComboBox(list.toArray());
        TableColumn tc = tblStavke.getColumnModel().getColumn(0);
        tc.setCellEditor(new DefaultCellEditor(cb));
    }
